function writeM2b(root, cfgM1, cfgM2, R)

cfgV = vidd.loadConfig();
[p, info] = vidd.calibrate(cfgV);
pM2 = lc.calibrate(cfgM2);
nf = lc.normalizedFolds(pM2);

f = fopen(fullfile(root,'results','summary_2b.md'), 'w');
c = onCleanup(@() fclose(f));

w(f, '# Results — Model 2b: VIDD Capacity Dynamics\n');
w(f, '_Auto-generated by `writeSummary.m`. Every number is recomputed at write time._\n');

w(f, '\n## The result, and the honest boundary around it\n');
w(f, ['\nModel 2b makes Model 2''s capacity ceiling C_max endogenous: how a patient is ', ...
      'ventilated moves the diaphragm activity A, which moves the capacity the weaning fold ', ...
      'is defined against. Put in the same units as the load axis, the tracheostomy''s effect ', ...
      'on capacity **can exceed** its effect on breathing load — but only if it raises ', ...
      'activity, and by how much is the one number nobody has measured.\n']);

w(f, '\n## Two spec hypotheses the literature overturned\n\n');
w(f, '| # | Spec said | Data say | Source |\n|---|---|---|---|\n');
w(f, ['| H3 | disease catabolism is an activity-independent drain no strategy can rescue | ', ...
      'sepsis is a REVERSIBLE starting offset; septic diaphragm force **rose 19%%** over 4 d ', ...
      'while ventilated | Lecronier 2022 PMID 35403916; Demoule 2013 PMID 23641946 |\n']);
w(f, ['| U-shape | degradation g(A) is U-shaped (both extremes worsen atrophy) | atrophy RATE ', ...
      'is **monotonic** in support; the U exists only in clinical OUTCOME, not in dC/dt | ', ...
      'Zambon 2016 PMID 26992064 vs Goligher 2018 PMID 28930478 |\n']);
w(f, ['\nBoth spec versions are retained as switchable modes (`g_mode`, `d_mode`) so the ', ...
      'contrast is a result, not a silent choice.\n']);

w(f, '\n## Calibration\n\n');
w(f, '| parameter | value | basis |\n|---|---|---|\n');
w(f, '| k_deg | %.4f /day | **anchored** — Jaber 2010 (PMID 20813887): twitch pressure −32%% at 6 d |\n', info.k_deg);
w(f, '| g_min, p_mono | %.3f, %.3f | **solved** — Zambon 2016''s atrophy rates by mode |\n', info.g_min, info.p_mono);
w(f, '| k_syn | %.4f /day | **solved** — Lecronier 2022 two-group relaxation |\n', info.k_syn);
w(f, ['\n**Three independent checks land.** Jaber (not used to fit anything but the rate) is ', ...
      'reproduced: model %.0f%% loss at 6 d vs measured 32%%. The Lecronier two-group solve ', ...
      'implies a sepsis offset of %.0f%%, against Demoule''s independent %.0f%% — agreeing to ', ...
      '%.1f percentage points. Timescale separation from Model 2 is %.0f–%.0f×, so the ', ...
      'quasi-static coupling holds.\n'], ...
    100*info.jaber_predicted_loss_at_A0, 100*info.lecronier_septic_offset, ...
    100*info.demoule_septic_offset, 100*info.offset_agreement, 14, 46);
w(f, ['\n**Levine 2008 was deliberately NOT used** — its 18–69 h window implies a rate 4–17× ', ...
      'every other study. It was the obvious anchor and it would have been wrong by an order ', ...
      'of magnitude.\n']);

if isfield(R,'vidd_exp5')
    w(f, '\n## The capstone caveat: one unmeasured number carries the result\n');
    T = R.vidd_exp5;
    loadE = T.load_effect_cmH2O(1);
    ratioAssumed = interp1(T.A_gap, T.ratio, 0.30);
    ratioSmall   = interp1(T.A_gap, T.ratio, 0.10);
    crossGap = interp1(T.ratio, T.A_gap, 1.0);
    w(f, ['\nThe tracheostomy''s capacity effect versus its load effect depends entirely on the ', ...
          'ETT→trach increase in diaphragm activity (the "A-gap"). That number has **never been ', ...
          'measured**: no study measures diaphragm activity across the ETT-vs-tracheostomy ', ...
          'contrast. What IS supported, at RCT grade, is that tracheostomy reduces SEDATION ', ...
          '(TracMan 5 vs 8 sedation-days; Meng meta WMD −6 d) — the direction of the A change, ', ...
          'not its magnitude.\n']);
    w(f, '\n| A-gap | capacity/load ratio | interpretation |\n|---|---|---|\n');
    w(f, '| ~%.2f | 1.0 | the two channels are equal |\n', crossGap);
    w(f, '| 0.10 (small, sedation-plausible) | %.1f× | capacity already comparable-to-larger |\n', ratioSmall);
    w(f, '| 0.30 (the assumed doubling) | %.1f× | the headline "5× larger" figure |\n', ratioAssumed);
    w(f, ['\n**The robust statement:** the capacity channel overtakes the load channel above an ', ...
          'A-gap of only ~%.2f, so a large doubling is NOT required for capacity to matter as ', ...
          'much as load — any modest, RCT-supported increase in activity suffices. The specific ', ...
          '"5×" needs the large end of the gap and should be reported as an upper bound, not a ', ...
          'point estimate.\n'], crossGap);
    w(f, '\n![A-gap](figures/fig_vidd_Agap.png)\n');
end

w(f, '\n## What this does to the programme''s thesis\n');
w(f, ['\nIf capacity is reversible and activity-sensitive (Lecronier) and the device acts on it ', ...
      'at least as much as on load, then the weaning–survival paradox cannot be explained by ', ...
      '"capacity is beyond rescue". The explanation shifts to what the other models already ', ...
      'show: the load-axis rescue window is narrow (Model 2), the capacity-axis benefit ', ...
      'depends on sedation practice actually changing (this model''s open question), and any ', ...
      'benefit competes against the device''s costs (Model 3) and against competing causes of ', ...
      'death. That is a sharper and more testable account than the spec''s original one.\n']);

w(f, '\n## Figures\n\n');
w(f, '| file | shows |\n|---|---|\n');
w(f, '| `fig_vidd_shape.png` | H1 — capacity vs activity, monotonic vs U, and the shape-is-g test |\n');
w(f, '| `fig_vidd_trajectory.png` | H2 — capacity trajectories by strategy, Jaber anchor, reversibility |\n');
w(f, '| `fig_vidd_catabolic.png` | H3 — sepsis as reversible offset (data) vs drain (spec) |\n');
w(f, '| **`fig_vidd_dynamic_rescue.png`** | **H4 — the device on both axes over time (capstone)** |\n');
w(f, '| `fig_vidd_Agap.png` | the unmeasured A-gap, stress-tested |\n');

w(f, '\n## Outstanding\n\n');
w(f, '- **The A-gap is the critical unmeasured parameter.** Diaphragm activity has never been measured ETT vs trach. This is the single most valuable future measurement for the programme.\n');
w(f, '- C ∝ cross-sectional area, and twitch-pressure rates transferred to the MIP scale as fractional changes — both declared assumptions, neither demonstrated for the diaphragm in vivo (Yamada 2024 is contrary).\n');
w(f, '- The support→A mapping is uncalibrated; where each scenario sits on the A axis inherits it.\n');
w(f, '\nFull provenance: [docs/CALIBRATION.md](../docs/CALIBRATION.md)\n');
end

