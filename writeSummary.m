function writeSummary(R, only)
%WRITESUMMARY Regenerate results/summary.md and results/summary_m2.md.
%
%   Every number here is recomputed from the model at write time, not
%   transcribed. A summary that can drift from the code it describes is
%   worse than no summary.

arguments
    R    (1,1) struct = struct()
    only (1,1) string = "all"
end

root = wob.projectRoot;
cfg1 = wob.loadConfig();
cfg2 = lc.loadConfig();
[p, info] = lc.calibrate(cfg2);
nf = lc.normalizedFolds(p);

if ismember(only, ["all","m1"]),  writeM1(root, cfg1, R); end
if ismember(only, ["all","m2"]),  writeM2(root, cfg1, cfg2, p, info, nf, R); end
if ismember(only, ["all","m1b"]) && isfile(fullfile(root,'config','params_co2.json'))
    writeM1b(root, cfg1, R);
end
if ismember(only, ["all","m2b"]) && isfile(fullfile(root,'config','params_vidd.json'))
    writeM2b(root, cfg1, cfg2, R);
end
end

