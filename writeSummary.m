function writeM2Caveats(f)
w(f, '\n## Caveats that belong in the manuscript\n\n');
w(f, ['- **`s` is weakly identified.** l_high is capped by u_crit, so the reachable part of the ', ...
      'Vassilakopoulos band is (0.310, 0.400) and the discrimination only bounds s from *below* ', ...
      '(s > 23). The base case takes the midpoint of the reachable band; targeting the midpoint ', ...
      'of the full band would force s ≈ 354 — an implausibly hard threshold, and an over-reading ', ...
      'of the data.\n']);
w(f, ['- **`L_device = f_device × L_total` is an approximation.** f_device is a fraction of ', ...
      '*work*; L is a *pressure*. They coincide only if the pressure components stay in fixed ', ...
      'proportion, which they do not (resistive terms peak at peak flow, elastic at ', ...
      'end-inspiration). Measured overstatement: ~10%%. The rescue window does not use the ', ...
      'decomposition, so its conclusions are unaffected.\n']);
w(f, ['- **β''s calibration borrows a healthy-subject fatigue protocol.** Equating Laghi''s ', ...
      'capacity drop with the model''s low attractor is an interpretation, not a measurement. ', ...
      'It is nonetheless the only quantitative anchor available, and far better than an ', ...
      'arbitrary value.\n']);
w(f, ['- **Laghi 2003 (PMID 12411288) is a direct challenge.** Weaning failure was *not* ', ...
      'accompanied by low-frequency diaphragm fatigue: twitch Pdi was unchanged (8.9→9.4 cmH₂O), ', ...
      'and patients were removed from the trial *before* fatigue developed — weakness, not ', ...
      'fatigue, dominated. Confront this head-on. One defensible reading: the low branch is a ', ...
      'state patients are removed from before reaching, so the bifurcation governs **the decision ', ...
      'to stop**, not the physiological collapse itself.\n']);
w(f, ['- **Right fold vs left fold.** This model uses the *right* fold, which is the SBT ', ...
      'question: between trials the patient is supported (L≈0) and recovers towards C_max, so ', ...
      'each trial starts on the high branch and the question is whether it survives the load. ', ...
      'The *left* fold governs a different, also real scenario — a patient ground down over days ', ...
      'without full unloading. Hysteresis then says something clinically pointed: once collapsed, ', ...
      'restoring the ETT-level load is **not enough**; the load must fall below the left fold. ', ...
      'That is a dynamical argument for intervening early.\n']);
end

