function writeM1b(root, cfgM1, R)

cfgCO2 = co2.loadConfig();

f = fopen(fullfile(root,'results','summary_1b.md'), 'w');
c = onCleanup(@() fclose(f));

w(f, '# Results — Model 1b: CO₂ Kinetics and Ventilatory Demand\n');
w(f, '_Auto-generated by `writeSummary.m`. Every number is recomputed at write time._\n');

w(f, '\n## The one-line result\n');
if isfield(R,'co2_exp3')
    T = R.co2_exp3;
    lo = T(T.VCO2==min(T.VCO2) & T.VD_VT==min(T.VD_VT), :);
    hi = T(T.VCO2==max(T.VCO2) & T.VD_VT==max(T.VD_VT), :);
    w(f, ['\nSwapping an ETT for a tracheostomy saves **%.2f L/min** of ventilation — ', ...
          '**%.1f%%** of demand in the mildest case and **%.1f%%** in the sickest. The ', ...
          'gas-exchange axis says what the mechanical axis said: the device moves little, ', ...
          'and least where disease is worst.\n'], ...
        lo.dVE_device, 100*lo.frac_device, 100*hi.frac_device);
end

w(f, '\n## What this model adds to Model 1\n');
w(f, ['\nModel 1 has SERIES dead space only (anatomic + apparatus). This model adds the ', ...
      '**alveolar** dead space of V/Q mismatch — the part that rises with lung injury and ', ...
      'that a V_D/V_T of 0.6 is actually measuring — and derives the required ventilation ', ...
      'from CO₂ production instead of taking it as a configured target.\n']);
w(f, ['\nThe series term is **not recomputed here**: `co2.deadSpaceTotal` calls Model 1''s ', ...
      '`wob.deadSpace`. The spec (§8) requires ΔV_D to be single-sourced; calling Model 1''s ', ...
      'function is the only way that stays true as either model changes, and it inherits ', ...
      'Model 1''s correction that the upper-airway bypass cancels between ETT and trach.\n']);

w(f, '\n### It closes Model 1''s largest open assumption\n');
cp = co2.coupling(cfgCO2, cfgM1, lc.loadConfig(), 'ETT_7_5', VCO2=300);
w(f, ['\nModel 1 carried `target_VA` as a hand derivation, and Sobol ranks it the **single ', ...
      'largest driver of total load** (ST = 0.56). This model computes it: at VCO₂ = 300 ', ...
      'mL/min and PaCO₂ = 40, V_A = **%.2f L/min** (Model 1 config: %.2f). The earlier ', ...
      'estimate of 6.3 was 2.7%% low. `tests/tModel1b.m` asserts the two do not drift apart.\n'], ...
    cp.target_VA_implied, cfgM1.patient.target_VA_L_min);

w(f, '\n## H1 — steady-state PaCO₂ (a counterfactual, and labelled as one)\n');
if isfield(R,'co2_exp1')
    T = R.co2_exp1;
    w(f, '\n| arm | dead space (mL) | series | alveolar | V_T (L) | V_E | PaCO₂ |\n|---|---|---|---|---|---|---|\n');
    for k = 1:height(T)
        w(f, '| %s | %.1f | %.1f | %.1f | %.3f | %.2f | %.1f |\n', T.arm{k}, ...
            T.Vd_total_mL(k), T.Vd_series_mL(k), T.Vd_alveolar_mL(k), T.V_T_L(k), T.V_E(k), T.PaCO2(k));
    end
    w(f, '\n- PaCO₂ gap, not intubated → trach: **%.2f mmHg**\n', T.PaCO2(1)-T.PaCO2(3));
    w(f, '- PaCO₂ gap, ETT → trach: **%.2f mmHg** — the device step\n', T.PaCO2(2)-T.PaCO2(3));
end
w(f, ['\n> **This is the mechanism with the controller switched off.** "At matched V_E the ', ...
      'tracheostomy gives a lower PaCO₂" is true of the alveolar gas equation and is not ', ...
      'what happens to a patient. Chadda 2002 measured both states in the same subjects: ', ...
      'removing 74 mL of dead space left PaCO₂ and respiratory rate **unchanged** and raised ', ...
      'V_T from 330 to 400 mL. PaCO₂ is regulated; ventilation is the response.\n']);
w(f, '\n![steady state](figures/fig_co2_steady_state.png)\n');

w(f, '\n## H2 — required ventilation, and what it costs\n');
w(f, ['\nThe chain runs end to end: CO₂ kinetics set V_E, Model 1 turns V_E into effort, ', ...
      'Model 2 takes the effort as load.\n']);
w(f, '\n![required VE](figures/fig_co2_required_ve.png)\n');

w(f, '\n## H3 — the dilution (thesis figure)\n');
w(f, ['\nThe spec''s H3 would, read as ETT→trach, be measuring the apparatus term and ', ...
      'restating Model 1''s finding. It is redefined as the **CO₂-side dilution**: how the ', ...
      'ventilatory saving''s share shrinks as CO₂ production and V/Q mismatch rise. That is ', ...
      'the gas-exchange analogue of Model 1''s `f_device`, on a genuinely new axis.\n']);
if isfield(R,'co2_exp3')
    T = R.co2_exp3;
    w(f, '\nShare of ventilatory demand attributable to the dead-space saving:\n\n');
    w(f, '| contrast | mildest (VCO₂ %g, V_D/V_T %.2f) | sickest (VCO₂ %g, V_D/V_T %.2f) |\n|---|---|---|\n', ...
        min(T.VCO2), min(T.VD_VT), max(T.VCO2), max(T.VD_VT));
    lo = T(T.VCO2==min(T.VCO2) & T.VD_VT==min(T.VD_VT), :);
    hi = T(T.VCO2==max(T.VCO2) & T.VD_VT==max(T.VD_VT), :);
    w(f, '| not intubated → trach | %.1f%% | %.1f%% |\n', 100*lo.frac_bypass, 100*hi.frac_bypass);
    w(f, '| **ETT → trach** | **%.1f%%** | **%.1f%%** |\n', 100*lo.frac_device, 100*hi.frac_device);
    w(f, ['\nBoth contrasts fall monotonically in both directions. The device step is only ', ...
          '**%.0f%%** of the bypass step — the 72 mL literature is about the wrong comparison ', ...
          'for a clinician already holding an ETT.\n'], 100*lo.dVE_device/lo.dVE_bypass);
end
w(f, '\n![dilution](figures/fig_co2_dilution_grid.png)\n');

w(f, '\n## The transient — and why the negative studies found nothing\n');
if isfield(R,'co2_exp4')
    w(f, ['\nWhat happens after tracheostomy depends entirely on what the patient does with ', ...
          'the ventilation that dead space just freed:\n\n']);
    w(f, '| assumption | PaCO₂ | V_E |\n|---|---|---|\n');
    w(f, '| V_E held fixed | falls ~1.3 mmHg | unchanged |\n');
    w(f, '| **PaCO₂ regulated (Chadda)** | **unchanged** | **falls ~0.22 L/min** |\n');
    w(f, ['\n**The dead-space benefit shows up in effort, not in blood gas.** So a study ', ...
          'looking for it in PaCO₂ or V_D/V_T finds nothing — and that absence is not evidence ', ...
          'the benefit is absent. It is the same reading that reconciles Mohr 2001 and ', ...
          'Joseph 2013 with the model.\n']);
    w(f, ['\n**Why two compartments.** The lung equilibrates in ~35 s but the body CO₂ stores ', ...
          'take ~35 min, so the half-time to the new PaCO₂ steady state is ~55 min. A ', ...
          'single-compartment model would report ~0.4 min — wrong by two orders of magnitude.\n']);
end
w(f, '\n![transient](figures/fig_co2_transient.png)\n');

w(f, '\n## Load is a product, not a sum\n');
w(f, ['\nRaising metabolism alone does not create a failing patient: in near-normal mechanics ', ...
      '(C_rs = 0.05, R_aw = 3) the ETT load peaks at ~14 cmH₂O even at VCO₂ = 450, below the ', ...
      'observed weaning-failure value of 19.5. With severe mechanics (C_rs = 0.02, R_aw = 15) ', ...
      'it reaches 19.5 at VCO₂ = 250. **Neither axis alone gets there** — which is why Model 1 ', ...
      'needed metabolic rate added to its severity grid, and why that grid is now three-dimensional.\n']);

w(f, '\n## Deviations from the Model 1b spec\n\n');
w(f, '| # | Spec said | Model does | Why |\n|---|---|---|---|\n');
w(f, ['| 1 | `V_D_total = V_D_phys + V_D_app − ΔV_D_bypassed(device)` | series from Model 1, ', ...
      'bypass cancels | Both tubes bypass the same airway (Model 1, CALIBRATION.md §1.2). ', ...
      'Read as written, H3 would restate Model 1 rather than extend it. |\n']);
w(f, ['| 2 | alveolar dead space implicitly ∝ V_T | **∝ (V_T − V_D_series)** | As a fraction ', ...
      'of V_T, `V_E = V_A_req/(1 − V_D/V_T)` contains **no device term at all** — swapping the ', ...
      'tube would have exactly zero effect and H3 would be vacuous. Regression-tested. |\n']);
w(f, ['| 3 | `S_body: 15.0` (no unit) | **15 mL/(kg·mmHg)** | Read as an absolute capacitance ', ...
      'it gives a 0.5 min body time constant, contradicting the spec''s own §2.1 ("large → slow, ', ...
      'minutes to hours") and the reason for having two compartments at all. Per-kg gives ~35 min. |\n']);
w(f, ['| 4 | H1 stated as a prediction | **labelled a counterfactual** | Chadda measured PaCO₂ ', ...
      '**unchanged**; the patient re-regulates. H2 is the clinical prediction. |\n']);

w(f, '\n## Outstanding\n\n');
w(f, '- `S_body`, `diss_slope`, `diss_intercept` — TODO-verify; order-of-magnitude only. Affect the transient, not H1–H3.\n');
w(f, '- `VCO2` and `V_D/V_T` ranges are grade C (textbook physiology), not independently verified.\n');
w(f, '- The CO₂ dissociation curve is linearised; `co2.twoCompartment` exposes the interface for replacing it.\n');
w(f, '\nFull provenance: [docs/CALIBRATION.md](../docs/CALIBRATION.md)\n');
end

