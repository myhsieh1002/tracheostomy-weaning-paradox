function w(f, varargin)
fprintf(f, varargin{:});
end

function s = tern(c,a,b)
if c, s=a; else, s=b; end
end

function s = gradeLabel(g)
switch g
    case 'A',   s = 'A — directly reported';
    case 'B',   s = 'B — derived here';
    case 'A/B', s = 'A/B — mixed';
    case 'X',   s = '**X — UNVERIFIED**';
    otherwise,  s = g;
end
end

function s = truncate(str, n)
s = char(str);
if numel(s) > n, s = [s(1:n) '…']; end
s = strrep(s, '|', '\|');
end
