w(f, '\n## H2 — three regions, but no cusp where the spec put it\n');
w(f, ['\nThe fold loci in (L, C_max) are **exactly straight lines through the origin** ', ...
      '(L = %.4f·C_max and %.4f·C_max), because the system is scale-invariant in capacity. ', ...
      'Two lines through the origin meet only at the origin, so the bistable region is a ', ...
      '**wedge**, not a cusp.\n'], nf.l_low, nf.l_high);
w(f, ['\nThis matters for the argument: a cusp would mean bistability *ceases to exist* below ', ...
      'some capacity. It does not. The tipping point persists at every capacity — only its ', ...
      'position moves. That is why the rescue window slides rather than closing.\n']);
if isfield(R,'m2_exp2')
    w(f, '\nThe real cusp lives in the fatigue-threshold sharpness: below s* ≈ 23 the folds merge. ');
    w(f, 'The calibrated s = %.1f sits %.1f× above it.\n', p.s, p.s/23.17);
end
w(f, '\n![cusp](figures/fig_cusp_2p.png)\n');

w(f, '\n## H3 — the rescue window (thesis figure)\n');
w(f, '\n```\nsustainable branch exists  ⟺  L < l_high·C_max\n');
w(f, 'tracheostomy flips outcome ⟺  L_TRACH < l_high·C_max < L_ETT\n');
w(f, '                           ⟺  C_max ∈ ( L_TRACH/l_high , L_ETT/l_high )\n');
w(f, 'window width               =  ΔL_device / l_high\n```\n');
if isfield(R,'m2_exp3')
    T = R.m2_exp3;
    w(f, '\n| V_A (L/min) | VCO₂ | C_rs | L_ETT | L_TRACH | ΔL | window in C_max | reaches observed MIP? |\n');
    w(f, '|---|---|---|---|---|---|---|---|\n');
    VAs = unique(T.target_VA)';
    for va = VAs
        for cr = [0.02 0.04 0.06]
            [~,j] = min(abs(T.C_rs - cr)); sel = find(T.target_VA==va & T.C_rs==T.C_rs(j),1);
            w(f, '| %.1f | %d | %.2f | %.2f | %.2f | %.2f | (%.1f, %.1f) | %s |\n', ...
                T.target_VA(sel), T.VCO2(sel), T.C_rs(sel), T.L_ETT(sel), T.L_TRACH(sel), ...
                T.dL_device(sel), T.window_Cmax_low(sel), T.window_Cmax_high(sel), ...
                tern(T.overlaps_observed_MIP(sel),'**yes**','no'));
        end
    end
    w(f, '\n**Three findings that depart from the spec.**\n\n');
    w(f, ['1. **The window moves, it does not shrink.** Width = ΔL_device/l_high, and ΔL_device ', ...
          'is near-constant across severity, so the band keeps its width (~%.1f–%.1f cmH₂O) and ', ...
          'slides upward as load rises. The spec predicted a window collapsing to nothing at low ', ...
          'C_max; that does not happen.\n'], min(T.window_width), max(T.window_width));
    w(f, ['2. **Metabolic rate decides whether the window is even reachable.** At a normal ', ...
          'V_A = 4.2 L/min the model cannot reach the loads measured in failing weaning patients ', ...
          '(Vassilakopoulos: Pi = 19.5 cmH₂O) and the window falls *below* the observed MIP range ', ...
          'entirely — a spurious "the device never matters" result that is an artefact of ', ...
          'assuming normal metabolism. The spec did not treat V_A as a severity axis; it is one, ', ...
          'and Sobol ranks it the single largest driver of total load.\n']);
    w(f, ['3. **The window is narrow wherever it sits**: mean %.1f cmH₂O against a clinical ', ...
          'C_max range of 20–70, i.e. **~%.0f%% of the capacity axis**.\n'], ...
        mean(T.window_width), 100*mean(T.window_width)/50);
    w(f, ['\n**The paradox, quantified.** For a tracheostomy to decide a patient''s outcome, that ', ...
          'patient''s capacity must land inside a band a few cmH₂O wide whose position depends on ', ...
          'their compliance, airway resistance *and* metabolic rate — none of which is known ', ...
          'precisely at the bedside. An unselected trial puts few patients in the window and ', ...
          'averages to approximately nothing.\n']);
end
w(f, '\n![rescue](figures/fig_rescue_window.png)\n');

