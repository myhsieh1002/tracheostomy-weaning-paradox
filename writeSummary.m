w(f, '\n## Calibrated parameters\n\n');
w(f, '| parameter | value | basis |\n|---|---|---|\n');
w(f, '| α | %.4f /hr | Laghi 1995 (PMID 7592215), 24 h recovery |\n', p.alpha);
w(f, '| β | %.4f /hr | **solved** from Laghi''s capacity drop: C_low/C_max = α/(α+β) = %.3f |\n', p.beta, info.capacityDrop);
w(f, '| s | %.2f | **solved** so the fold separates the Vassilakopoulos groups |\n', p.s);
w(f, '| u_crit | %.2f | Roussos & Macklem 1977 (PMID 893274), Pdicrit — a **pure pressure ratio** |\n', p.u_crit);
w(f, ['\n> The build spec anchored `u_crit` to the tension-time index (0.15). That is invalid: ', ...
      'TTdi carries a duty-cycle factor that `u = L/C` does not, so 0.15 understates the ', ...
      'threshold roughly threefold. With the spec''s placeholders (β=0.8, s=25) the fold sits ', ...
      'at l_high = 0.225 and the model predicts **failure for the patients who actually weaned**.\n']);

w(f, '\n## H1 — bistability and hysteresis\n\n');
C_max = 50;
w(f, 'At C_max = %g cmH₂O: folds at L = **[%.2f, %.2f]** cmH₂O, hysteresis width %.2f.\n', ...
    C_max, nf.l_low*C_max, nf.l_high*C_max, (nf.l_high-nf.l_low)*C_max);
